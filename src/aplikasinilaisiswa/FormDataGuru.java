     void showData() {
        try {
            tabModel = (DefaultTableModel)tabel.getModel();
            tabModel.setRowCount(0);
            k.setDriver();
            k.query = "select * from guru";
            k.read();
            while (k.rs.next()) {
                d = k.rs.getString("id_guru");
                y = k.rs.getString("nama_guru");
                g = k.rs.getString("username");
                h = k.rs.getString("password");
                i = k.rs.getString("jenis_kelamin");
                String[]data = {d ,y ,g ,h ,i};
                tabModel.addRow(data);
            }
        } catch (Exception ex) {
            System.out.println("Error" + ex);
        }
    }
     
