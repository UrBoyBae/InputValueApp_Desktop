    void create() {
        if(pria.isSelected()) {
            jk = pria.getText();
        }
        else {
            jk = wanita.getText();
        }
        
        a = idguru.getText();
        b = namaguru.getText();
        c = password.getText();
        z = username.getText();
    }
    
    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        if ((idguru.getText().isEmpty()) && (namaguru.getText().isEmpty())&& (username.getText().isEmpty()) && (password.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Isi Data Terlebih Dahulu", "info", JOptionPane.WARNING_MESSAGE);
        }
        else if ((idguru.getText().equals("")) || (namaguru.getText().equals("")) || (username.getText().equals("")) || (password.getText().equals(""))) {
            JOptionPane.showMessageDialog(null, "lengkapi Data Terlebih Dahulu", "info", JOptionPane.WARNING_MESSAGE);
        }
        else {
            try {
                create();
                k.setDriver();
                k.query = "insert into guru values(?,?,?,?,?)";
                k.CUD();
                k.ps.setString(1, a);
                k.ps.setString(2, b);
                k.ps.setString(3, z);
                k.ps.setString(4, c);
                k.ps.setString(5, jk);
                k.ps.execute();
                JOptionPane.showMessageDialog(null, "Data Tersimpan","Info", JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Data Gagal Tersimpan");
                System.out.println("Error" + ex);
            }

            clearForm();
            showData();
        }

    }//GEN-LAST:event_simpanActionPerformed

    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
        // TODO add your handling code here:
        tabModel = (DefaultTableModel) tabel.getModel();
        //cek jika row sudah terisi data minimal ada 1 data
        if (tabel.getSelectedRowCount() == 1) {
            r = idguru.getText();

            int s = JOptionPane.showConfirmDialog(null, "Yakin Ingin Menghapus Data Ini?","Warning", JOptionPane.YES_NO_OPTION);

            if (s == JOptionPane.YES_OPTION) {
                try {
                    k.setDriver();
                    k.query = "delete from guru where id_guru='"+r+"'";
                    k.CUD();
                    k.ps.executeUpdate();
                    clearForm();
                    showData();
                } catch (Exception e) {
                    System.err.println("error" + e);
                }
            }

        }
        else {
            JOptionPane.showMessageDialog(null, "Tidak ada data yang dipilih", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteMouseClicked

    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked
        // TODO add your handling code here:
        tabModel = (DefaultTableModel) tabel.getModel();
        //cek jika row sudah terisi data minimal ada 1 data
        if (tabel.getSelectedRowCount() == 1) {
            m = idguru.getText();
            n = namaguru.getText();
            x = username.getText();
            o = password.getText();

            if(pria.isSelected()) {
                p = pria.getText();
            }
            else {
                p = wanita.getText();
            }

            int q = JOptionPane.showConfirmDialog(null, "Yakin Ingin Mengubah Data Ini?","Warning", JOptionPane.YES_NO_OPTION);

            if (q == JOptionPane.YES_OPTION) {
                try {
                    k.setDriver();
                    k.query = "update guru set id_guru='"+m+"',nama_guru='"+n+"',username='"+x+"',password='"+o+"',jenis_kelamin='"+p+"' where id_guru='"+m+"'";
                    k.CUD();
                    k.ps.executeUpdate();
                } catch (Exception e) {
                    System.err.println("error" + e);
                }
            }

        }
        else {
            JOptionPane.showMessageDialog(null, "Tidak ada data yang dipilih", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        clearForm();
        showData();
    }//GEN-LAST:event_editMouseClicked

